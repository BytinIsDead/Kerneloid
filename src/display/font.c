/* Additional extended characters (0x80-0xFF) - simplified */
static const uint8_t vga_font_extended[128][16] = {
    /* Euro sign */
    {0x00, 0x00, 0x00, 0x00, 0xFC, 0xC6, 0xC0, 0xC0, 0xFC, 0xC0, 0xC0, 0xC0, 0xC0, 0x00, 0x00, 0x00},
};

/* Current font style */
static font_style_t current_style = {
    .foreground = 0xFFFFFF,
    .background = 0x000000,
    .flags = FONT_FLAG_NONE,
    .transparent = false
};

/* Font initialized flag */
static int font_initialized = 0;

/*
 * Initialize font subsystem
 */
void font_init(void) {
    if (font_initialized) {
        return;
    }
    
    font_initialized = 1;
    kprintf("[FONT] Font subsystem initialized (8x16 VGA font)\n");
}

/*
 * Get font bitmap data for a character
 */
const uint8_t* font_get_char(unsigned char c) {
    if (c < 128) {
        return vga_font_8x16[c];
    } else if (c < 256) {
        return vga_font_extended[c - 128];
    }
    return vga_font_8x16[0];  /* Return null for unknown */
}

/*
 * Get character width
 */
int font_get_char_width(unsigned char c) {
    (void)c;
    return FONT_WIDTH;
}

/*
 * Get string width
 */
int font_get_string_width(const char* str) {
    if (!str) return 0;
    return strlen(str) * FONT_WIDTH;
}

/*
 * Draw a single character
 */
void font_draw_char(int x, int y, unsigned char c, uint32_t fg_color, uint32_t bg_color) {
    const uint8_t* bitmap = font_get_char(c);
    
    for (int row = 0; row < FONT_HEIGHT; row++) {
        uint8_t line = bitmap[row];
        for (int col = 0; col < FONT_WIDTH; col++) {
            if (line & (0x80 >> col)) {
                fb_put_pixel(x + col, y + row, fg_color);
            } else if (!current_style.transparent) {
                fb_put_pixel(x + col, y + row, bg_color);
            }
        }
    }
}

/*
 * Draw a string
 */
void font_draw_string(int x, int y, const char* str, uint32_t fg_color, uint32_t bg_color) {
    if (!str) return;
    
    int x_pos = x;
    while (*str) {
        font_draw_char(x_pos, y, *str, fg_color, bg_color);
        x_pos += FONT_WIDTH;
        str++;
    }
}

/*
 * Draw string with word wrapping
 */
int font_draw_string_wrap(int x, int y, int max_width, const char* str, 
                          uint32_t fg_color, uint32_t bg_color) {
    if (!str || max_width <= 0) return 0;
    
    int lines = 1;
    int x_pos = x;
    const char* word_start = str;
    int word_width = 0;
    
    while (*str) {
        if (*str == ' ' || *str == '\n' || *str == '\t') {
            /* Draw accumulated word */
            if (word_width > 0) {
                if (x_pos + word_width > x + max_width && x_pos > x) {
                    /* New line */
                    y += FONT_HEIGHT;
                    x_pos = x;
                    lines++;
                }
                font_draw_string(x_pos, y, word_start, fg_color, bg_color);
                x_pos += word_width;
                word_start = str + 1;
                word_width = 0;
            }
            
            if (*str == '\n') {
                y += FONT_HEIGHT;
                x_pos = x;
                lines++;
            } else {
                x_pos += FONT_WIDTH;
                if (x_pos > x + max_width) {
                    y += FONT_HEIGHT;
                    x_pos = x + FONT_WIDTH;
                    lines++;
                }
            }
            word_start = str + 1;
        } else {
            word_width += FONT_WIDTH;
        }
        str++;
    }
    
    /* Draw remaining word */
    if (word_width > 0) {
        if (x_pos + word_width > x + max_width && x_pos > x) {
            y += FONT_HEIGHT;
            lines++;
        }
        font_draw_string(x_pos, y, word_start, fg_color, bg_color);
    }
    
    return lines;
}

/*
 * Draw string centered
 */
void font_draw_string_centered(int x, int y, int width, const char* str,
                               uint32_t fg_color, uint32_t bg_color) {
    if (!str) return;
    
    int str_width = font_get_string_width(str);
    int x_offset = x + (width - str_width) / 2;
    if (x_offset < x) x_offset = x;
    
    font_draw_string(x_offset, y, str, fg_color, bg_color);
}

/*
 * Set font style
 */
void font_set_style(const font_style_t* style) {
    if (style) {
        current_style = *style;
    }
}

/*
 * Get current font style
 */
font_style_t* font_get_style(void) {
    return &current_style;
}

/*
 * Draw character using current style
 */
void font_draw_charStyled(int x, int y, unsigned char c) {
    font_draw_char(x, y, c, current_style.foreground, current_style.background);
}

/*
 * Draw string using current style
 */
void font_draw_stringStyled(int x, int y, const char* str) {
    font_draw_string(x, y, str, current_style.foreground, current_style.background);
}

